package com.sonnhe.voicecommand.voicelib.model;

/**
 * 语义结果
 */
public class SemanticResult {
    private String name;
    private String url;

    public SemanticResult() {
        super();
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
